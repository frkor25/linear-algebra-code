import numpy as np

# Define the matrix A as a NumPy array
A = np.array([[1, -5, 4, 0],
              [2, 1, 0, 6]])

# Compute the transpose
A_transpose = A.T

# Print the result
print("-" * 50)
print("Matrix A:")
print(A)
print("-" * 50)
print("Transpose of A:")
print(A_transpose)
print("=" * 50)
